import tkinter.messagebox as messagebox
import tkinter as tk
from tkinter.constants import SUNKEN
cal = tk.Tk()
cal.title("Calculator")

frame = tk.Frame(master=cal, bg="white", padx=20)
frame.pack()
entry = tk.Entry(master=frame, width=30, borderwidth=5, relief=SUNKEN)
entry.grid(row=0, column=0, columnspan=3, padx=10, pady=10)
def button_click(number):
    entry.insert(tk.END, number)
def result():
    try:
        y = str(eval(entry.get()))
        entry.delete(0,tk.END)  
        entry.insert(0,y)
    except:
        tkinter.messagebox.showinfo("Error", "Invalid Input")
def clear():
    entry.delete(0, tk.END)
    
button_1 = tk.Button(master=frame, text="1", padx=15, pady=5, command=lambda: button_click(1))
