button_subtract = tk.Button(master=frame, text="-", padx=15, pady=5, command=lambda: button_click("-"))    
button_multiply = tk.Button(master=frame, text="*", padx=15, pady=5, command=lambda: button_click("*"))
button_divide = tk.Button(master=frame, text="/", padx=15, pady=5, command=lambda: button_click("/"))
button_equal = tk.Button(master=frame, text="=", padx=15, pady=5, width=30, command=result)
button_clear = tk.Button(master=frame, text="Clear", padx=15, pady=5, width=30,command=clear)
button_add.grid(row=1, column=3, pady=2)
